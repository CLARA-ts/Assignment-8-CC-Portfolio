function setup() {
  createCanvas(400, 1000);
}

function draw() {
  background(220);
  drawperson(200,90)
  function drawperson(x,y){
    push()
    fill("black")
    noStroke()
    arc(x+5,y-20,110,120,0.5*PI,1.5*PI)
    fill(220)
    arc(x+10,y-40,40,80,0.5*PI,1.5*PI)
    fill("black")
    rect(x-30,y+25,40)
    circle(x,y,80)
    triangle(x+40,y,x,y+40,x+100,y+60)
    stroke("gold")
    strokeWeight(2)
    circle(x+20,y,20)
    ellipse(x+43,y,15,17)
    line(x-20,y+20,x+100,y+60)
    pop()
    push()
    stroke("rgb(201, 104, 104)")
    strokeWeight(2)
    fill(220)
    rect(x-80,y+55,140,200,20)
    line(x-80,y+70,x-180,y+245)
    line(x-80,y+140,x-140,y+245)
    circle(x-160,y+245,60)
    push()
    line(x-175,y+250,x-180,y+260)
    translate(10,10)
    line(x-175,y+250,x-180,y+260)
    translate(15,0)
    line(x-175,y+250,x-180,y+260)
    pop()
    line(x+60,y+70,x+160,y+245)
    line(x+60,y+140,x+120,y+245)
    circle(x+140,y+245,60)
    push()
    line(x+130,y+260,x+135,y+270)
    translate(10,0)
    line(x+130,y+260,x+135,y+270)
    translate(10,-3)
    line(x+130,y+260,x+135,y+270)
    pop()
    circle(x-40,y+100,20)
    circle(x-40,y+100,10)
    circle(x+20,y+100,20)
    circle(x+20,y+100,10)
    pop()
    push()
    stroke("black")
    strokeWeight(1.5)
    fill(177, 116, 87)
    rect(x-85,y+235,150,20,5)
    rect(x-85,y+255,150,20,5)
    fill("rgb(216, 210, 194)")
    quad(x-85,y+275,x+65,y+275,x+85,y+600,x-105,y+600)
    line(x-70,y+275,x-70,y+600)
    push()
    translate(25,0)
    line(x-70,y+275,x-70,y+600)
    translate(30,0)
    line(x-70,y+275,x-70,y+600)
    translate(30,0)
    line(x-70,y+275,x-70,y+600)
    translate(30,0)
    line(x-70,y+275,x-70,y+600)
    pop()
    pop()
   
  }
}